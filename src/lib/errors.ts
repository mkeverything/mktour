export type AppErrorCode = (typeof ERRORS)[keyof typeof ERRORS];

const APP_ERROR_CODES = new Set<string>(Object.values(ERRORS));

export const isAppErrorCode = (value: unknown): value is AppErrorCode =>
  APP_ERROR_CODES.has(value as string);

export class AppError extends Error {
  constructor(code: AppErrorCode, options?: ErrorOptions) {
    super(code, options);
    this.name = 'AppError';
  }

  get code(): AppErrorCode {
    return this.message as AppErrorCode;
  }
}

export const getAppErrorCode = (error: unknown): AppErrorCode => {
  if (error instanceof AppError) return error.code;
  if (error instanceof Error && isAppErrorCode(error.message)) {
    return error.message;
  }
  if (isAppErrorCode(error)) return error;
  return ERRORS.UNKNOWN_ERROR;
};

