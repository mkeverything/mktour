export const getAppErrorTrpcCode = (
  message: AppErrorMessage,
): TRPC_ERROR_CODE_KEY =>
  APP_ERROR_TRPC_CODES[message] ?? APP_ERROR_TRPC_CODES[ERRORS.UNKNOWN_ERROR]!;

export class AppError extends TRPCError {
  readonly code: TRPC_ERROR_CODE_KEY;

  constructor(message: AppErrorMessage, options?: ErrorOptions) {
    super({ code: getAppErrorTrpcCode(message), message, ...options });
    this.name = 'AppError';
    this.code = getAppErrorTrpcCode(message);
  }
}

export const getAppErrorMessage = (error: unknown): AppErrorMessage => {
  if (error instanceof AppError) return error.message as AppErrorMessage;
  if (error instanceof Error && isAppErrorMessage(error.message)) {
    return error.message;
  }
  if (isAppErrorMessage(error)) return error;
  return ERRORS.UNKNOWN_ERROR;
};
